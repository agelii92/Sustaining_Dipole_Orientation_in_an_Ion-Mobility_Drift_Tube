def u2kg(m):
    """given a mass in unit u, this function return the value given in SI unit of kg"""
    return m*1.66053904e-27

def nm2m(d):
    """given a distance in in unit nm, this function return the value given in SI unit of m"""
    return d*1e-9

def Å2m(d):
    """given a distance in unit Å, this function return the value given in SI unit of m"""
    return d*1e-10

def D2Cm(mu):
    """given a dipole moment in unit D, this function return the value given in SI unit of Cm"""
    return mu*3.33564e-30

def ps2s(t):
    """given a time in unit ps, this function return the value given in SI unit of s"""
    return t*1e-12




